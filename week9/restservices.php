        REST API
        Representational State Transfer
        PDO crud does most of this already

        With a REST interface ie a Web Service, instead of multiple URL you use
        a single URL as your interface

        In the interface you can provide:
        Create, read, update, delete

        When using the REST interface or API you use different HTTP verbs
        to specify what you want to do with the resource

        The resource is specified by the URL
        The action is specified by the HTTP verb
        
        We will use the following:
        POST - create records
        GET - read records
        PUT - updating records
        DELETE - deleting records
        
